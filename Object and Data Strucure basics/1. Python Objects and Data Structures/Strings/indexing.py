x = "Hello World"
print(x[0])  # indexing starts at 0.
print(x[-1])  # can go backwards
print(x[1:5])  # slicing gos from index 1 up to but not including 5
print(x[:5])  # slicing gos from index 0 up to but not including 5
y = "This is my string"
print(y[:10:2])  # slicing gos from index 0 up to but not including 10 every 2
print(y[::-1])  # reverses order from beginning to end and prints it

